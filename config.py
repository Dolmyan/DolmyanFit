# Базовые единицы времени в секундах.
# В тестовом режиме "час"/"минута"/"день" сжимаются, чтобы
# проверить всю воронку быстро.
if TEST_MODE:
    HOUR = 0.1          # 1 "час" воронки = 3 секунды
    MINUTE = 1        # 1 "минута" воронки = 1 секунда
    DAY = HOUR * 24
else:
    MINUTE = 60
    HOUR = 60 * 60
    DAY = HOUR * 24


