# ------------------------------------------------------------
#  TEST_MODE = True  -> все задержки воронки считаются в СЕКУНДАХ
#                       (можно прогнать всю цепочку за минуту)
#  TEST_MODE = False -> боевой режим, задержки в ЧАСАХ
# ------------------------------------------------------------
#  Управляется переменной окружения TEST_MODE (.env):
#    TEST_MODE=true  -> тестовый режим (вся воронка за минуту)
#    TEST_MODE=false -> боевой режим (по умолчанию!)
